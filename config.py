def get_resolution_profile(width: int, height: int) -> Dict:
    """–ü–æ–ª—É—á–∏—Ç—å –ø—Ä–æ—Ñ–∏–ª—å –¥–ª—è —É–∫–∞–∑–∞–Ω–Ω–æ–≥–æ —Ä–∞–∑—Ä–µ—à–µ–Ω–∏—è"""
    key = (width, height)
    if key in SCREEN_PROFILES:
        return SCREEN_PROFILES[key]
    else:
        # –ù–∞—Ö–æ–¥–∏–º –±–ª–∏–∂–∞–π—à–µ–µ —Ä–∞–∑—Ä–µ—à–µ–Ω–∏–µ
        closest = min(SCREEN_PROFILES.keys(), 
                     key=lambda k: abs(k[0]-width) + abs(k[1]-height))
        print(f"‚ö†Ô∏è –†–∞–∑—Ä–µ—à–µ–Ω–∏–µ {width}x{height} –Ω–µ –Ω–∞–π–¥–µ–Ω–æ, –∏—Å–ø–æ–ª—å–∑—É–µ–º {closest[0]}x{closest[1]}")
        return SCREEN_PROFILES[closest]

